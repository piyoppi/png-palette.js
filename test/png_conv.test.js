import Png from './../src/png';
import PngConv from './../src/png_conv';

const pixels = [
  0x00, 0xF0, 0x00, 0xFF,
  0xFF, 0x00, 0x00, 0xFF,
  0x00, 0xFF, 0x00, 0xFF,
  0x00, 0x00, 0xFF, 0xFF,
  0xFF, 0x00, 0x00, 0xFF,
  0x00, 0xFF, 0x00, 0xFF,
  0x00, 0x00, 0xFF, 0xFF,
  0x00, 0xF0, 0x00, 0xFF,
  0x00, 0x00, 0x00, 0xFF,
];

describe('fileData', () => {
  let png, pngConv;

  beforeEach(() => {
    png = new Png(pixels, 3, 3);
    pngConv = new PngConv();
    pngConv.png = png;
    pngConv.convert();
  });

  test('return raw png data', () => {
    expect(pngConv.fileData().bytes.toString()).toEqual([
      // PNG Signature
      0x89, 0x50, 0x4E, 0x47, 0x0D, 0x0A, 0x1A, 0x0A,
      // IDHR
      0x00, 0x00, 0x00, 0x0D,
      0x49, 0x48, 0x44, 0x52,
      0x00, 0x00, 0x00, 0x03,
      0x00, 0x00, 0x00, 0x03,
      0x08,
      0x03,
      0x00,
      0x00,
      0x00,
      0x61, 0xF6, 0x45, 0x8D,
      // PLTE
      0x00, 0x00, 0x00, 0x0F,
      0x50, 0x4C, 0x54, 0x45,
      0x00, 0xF0, 0x00,
      0xFF, 0x00, 0x00,
      0x00, 0xFF, 0x00,
      0x00, 0x00, 0xFF,
      0x00, 0x00, 0x00,
      0xE8, 0x45, 0xCA, 0x58,
      // IDAT
      0x00, 0x00, 0x00, 0x15,
      0x49, 0x44, 0x41, 0x54,
      0x78, 0x9C,
      0b10000000,
      0b00000001,
      0b10011111,
      0b11111110,
      0b10000000,
      0b00000000,
      0b00000000,
      0b00100000,
      0b01000000,
      0b00000000,
      0b01100000,
      0b00100000,
      0b01000000,
      0b00000000,
      0b01100000,
      0b00000000,
      0b10000000,
      0b00001011,
      0b01000000,
      0b00000010,
      0b00100000,
      0xAD, 0x49, 0x08, 0x45,
      // IEND
      0x00, 0x00, 0x00, 0x00,
      0x49, 0x45, 0x4E, 0x44,
      0xAE, 0x42, 0x60, 0x82
    ].toString());
  });
});
